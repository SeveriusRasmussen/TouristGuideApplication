/*package org.example.touristguideapplication;

Forklaring om filen .html

________________
<html>...</html>:
Defines the root element of an HTML page
________________
<head>...</head>
Contains meta-information about the HTML document, such as:
the title, character set, styles, scripts, etc.
ex:
<head>
    <title>My Web Page<titel>
    <!-- Other meta-information goes here -->
</head>
________________
<title>...</titel>
Sets the title of the HTML document,
which appears in the browser's title bar or tab.
ex:
<title>My Web Page</title>
________________
<body>...</body>
Contains the content of the HTML document,
such as text, images, links, and other elements.
ex:
<body>
    <h1>Hello, World!</h1>
    <p>This is a paragraph of text.</p>
    <!-- Other content goes here -->
</body>
________________
<h1> to <h6>...</h1> to </h6>
Define headings with decreasing levels of importance (from largest to smallest).
ex:
<h1>This is a Heading 1</h1>
<h2>This is a Heading 2</h2>
<!-- ... -->
<h6>This is a Heading 6</h6>
________________
<p>...</p>
Define a paragraph of text.
ex:
<p>This is a paragraph of text.</p>
________________
<a>...</a>
Creates a hyperlink (anchor) to another webpage or resource.
ex:
<a href="https://www.example.com">Visit Example.com</a>
________________
<img>
Embeds an image in the document
ex:
<img src="image.jrg" alt="Description of the image">
________________
<ul>, <ol>, <li>  ...  </ul>, </ol>, </li>
<ul>: defines an unordered (bulleted) list.
<ol>: defines an ordered (numbered) list.
<li>: defines a list item.
ex:
<ul>
    <li>Item 1</li>
    <li>Item 2</li>
</ul>
<ol>
    <li>First</li>
    <li>second</li>
</ol>
*/
